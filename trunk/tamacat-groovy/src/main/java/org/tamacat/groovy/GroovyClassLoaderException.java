package org.tamacat.groovy;

public class GroovyClassLoaderException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public GroovyClassLoaderException(String arg0) {
		super(arg0);
	}

	public GroovyClassLoaderException(Throwable arg0) {
		super(arg0);
	}

	public GroovyClassLoaderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
